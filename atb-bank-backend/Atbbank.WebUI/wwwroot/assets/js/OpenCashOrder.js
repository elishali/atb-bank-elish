function OpenCashOrder() {
    var cash_order = document.getElementById("cash_order");
    var OpenCashOrder = document.getElementById("OpenCashOrder");
    
    if (cash_order.style.display === "none") {
        cash_order.style.display = "block";

    } else {
        cash_order.style.display = "block";
        cash_order.style.display = "block";
    }
}

